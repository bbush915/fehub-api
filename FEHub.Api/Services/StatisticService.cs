        public StatisticValues GetValues(StatisticValueContext context)
        {
            var statisticValues = new StatisticValues()
            {
                HitPoints = GetValue(context, Statistics.HIT_POINTS),
                Attack = GetValue(context, Statistics.ATTACK),
                Speed = GetValue(context, Statistics.SPEED),
