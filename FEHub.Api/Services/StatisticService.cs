        }

        public int GetValue(StatisticValueContext context, Statistics statistic)
        {
            var baseStatisticValue = GetBaseValue(context, statistic);

            return baseStatisticValue.AdjustedBaseValue
                + GetSummonerSupportBonus(context, statistic)
                + GetRarityBonus(context, statistic)
                + GetLevelBonus(context, statistic)
                + GetMergeBonus(context, statistic)
                + GetDragonflowerBonus(context, statistic)
                + GetSkillBonus(context, statistic);
        }

        private static BaseStatisticValue[] GetBaseValues(StatisticValueContext context)
        {
            var baseStatisticValues = new BaseStatisticValue[] {
                GetBaseValue(context, Statistics.HIT_POINTS),
                GetBaseValue(context, Statistics.ATTACK),
                GetBaseValue(context, Statistics.SPEED),
                GetBaseValue(context, Statistics.DEFENSE),
                GetBaseValue(context, Statistics.RESISTANCE)
            };

            return baseStatisticValues;
        }

        private static BaseStatisticValue GetBaseValue(StatisticValueContext context, Statistics statistic)
        {
            int baseValue;
            int baseGrowthRate;
            int ordinal;

            switch (statistic)
            {
                case Statistics.HIT_POINTS:
                {
                    baseValue = context.Hero.BaseHitPoints;
                    baseGrowthRate = context.Hero.HitPointsGrowthRate;
                    ordinal = 1;

                    break;
                }

                case Statistics.ATTACK:
                {
                    baseValue = context.Hero.BaseAttack;
                    baseGrowthRate = context.Hero.AttackGrowthRate;
                    ordinal = 2;

                    break;
                }

                case Statistics.SPEED:
                {
                    baseValue = context.Hero.BaseSpeed;
                    baseGrowthRate = context.Hero.SpeedGrowthRate;
                    ordinal = 3;

                    break;
                }

                case Statistics.DEFENSE:
                {
                    baseValue = context.Hero.BaseDefense;
                    baseGrowthRate = context.Hero.DefenseGrowthRate;
                    ordinal = 4;

                    break;
                }

                case Statistics.RESISTANCE:
                {
                    baseValue = context.Hero.BaseResistance;
                    baseGrowthRate = context.Hero.ResistanceGrowthRate;
                    ordinal = 5;

                    break;
                }

                default:
                {
                    throw new Exception($"Unexpected statistic encountered: [{statistic}]");
                }
            }

            var adjustedBaseValue = baseValue;
            var adjustedBaseGrowthRate = baseGrowthRate;

            if (statistic == context.Asset)
            {
                adjustedBaseValue++;
                adjustedBaseGrowthRate += 5;
            }

            if (statistic == context.Flaw && context.Merges == 0)
            {
                adjustedBaseValue--;
                adjustedBaseGrowthRate -= 5;
            }

            var baseStatisticValue = new BaseStatisticValue()
            {
                AdjustedBaseGrowthRate = adjustedBaseGrowthRate,
                AdjustedBaseValue = adjustedBaseValue,
                BaseGrowthRate = baseGrowthRate,
                BaseValue = baseValue,
                Ordinal = ordinal,
                Statistic = statistic
            };

            return baseStatisticValue;
        }

        private static int GetRarityBonus(StatisticValueContext context, Statistics statistic)
        {
            var baseStatisticValues = GetBaseValues(context);

            var evenRarityIncrements = baseStatisticValues
                .Skip(1)
                .OrderByDescending(x => x.BaseValue)
                .ThenBy(x => x.Ordinal)
                .Take(2)
                .Select(x => x.Statistic);

            var rarityBonus = 0;

            if (statistic == Statistics.HIT_POINTS)
            {
                rarityBonus += context.Rarity >= 3 ? 1 : 0;
                rarityBonus += context.Rarity == 5 ? 1 : 0;
            }
            else
            {
                var rarityIncrement = evenRarityIncrements.Contains(statistic) ? 0 : 1;

                rarityBonus += context.Rarity >= 2 + rarityIncrement ? 1 : 0;
                rarityBonus += context.Rarity >= 4 + rarityIncrement ? 1 : 0;
            }

            return rarityBonus;
        }

        private static int GetLevelBonus(StatisticValueContext context, Statistics statistic)
        {
            var baseStatisticValue = GetBaseValue(context, statistic);

            if (context.Level == 1)
            {
                return 0;
            }

            if (context.Level == Constants.MaxLevel)
            {
                var growthValue = GetGrowthValue(context, baseStatisticValue.AdjustedBaseGrowthRate);
                return growthValue;
            }

            var growthVector = GetGrowthVector(context, baseStatisticValue);

            var levelBonus = 0;

            for (var i = 0; i < context.Level; i++)
            {
                levelBonus += growthVector[i] ? 1 : 0;
            }

            return levelBonus;
        }

        private static bool[] GetGrowthVector(StatisticValueContext context, BaseStatisticValue baseStatisticValue)
        {
            var growthValue = GetGrowthValue(context, baseStatisticValue.AdjustedBaseGrowthRate);
            var growthVectorId = GetGrowthVectorId(context, baseStatisticValue);

            if (growthValue < 1 || growthValue >= Constants.MaxLevel)
            {
                return Array.Empty<bool>();
            }

            var index = 2 * (64 * (growthValue - 1) + growthVectorId);

            var growthVector = new bool[40];

            var loBytes = GrowthVectors[index];

            for (var i = 1; i < 32; i++)
            {
                growthVector[i - 1] = Convert.ToBoolean((loBytes >> i) & 1);
            }

            var hiBytes = GrowthVectors[index + 1];

            for (var i = 0; i < 9; i++)
            {
                growthVector[(i - 1) + 32] = Convert.ToBoolean((hiBytes >> i) & 1);
            }

            return growthVector;
        }

        private static int GetGrowthValue(StatisticValueContext context, int adjustedBaseGrowthRate, int oldLevel = 1, int newLevel = Constants.MaxLevel)
        {
            var appliedGrowthRate = GetAppliedGrowthRate(context, adjustedBaseGrowthRate);
            return (int)Math.Floor(appliedGrowthRate / 100.0 * (newLevel - oldLevel));
        }

        private static int GetGrowthVectorId(StatisticValueContext context, BaseStatisticValue baseStatisticValue)
        {
            var appliedGrowthRate = GetAppliedGrowthRate(context, baseStatisticValue.AdjustedBaseGrowthRate);
            var statisticOffset = GetStatisticOffset(baseStatisticValue.Statistic);

            return (3 * (baseStatisticValue.BaseValue + 2) + appliedGrowthRate + context.Hero.BVID + statisticOffset) % 64;
        }

        private static int GetAppliedGrowthRate(StatisticValueContext context, int adjustedBaseGrowthRate)
        {
            var rarityFactor = GetRarityFactor(context.Rarity);
            return (int)Math.Floor(rarityFactor * adjustedBaseGrowthRate);
        }

        private static double GetRarityFactor(int rarity)
        {
            return 0.07 * rarity + 0.79;
        }

        private static int GetStatisticOffset(Statistics statistic)
        {
            switch (statistic)
            {
                case Statistics.HIT_POINTS:
                {
                    return -35;
                }

                case Statistics.ATTACK:
                {
                    return -28;
                }

                case Statistics.SPEED:
                {
                    return -21;
                }

                case Statistics.DEFENSE:
                {
                    return -14;
                }

                case Statistics.RESISTANCE:
                {
                    return -7;
                }

                default:
                {
                    throw new Exception($"Unexpected statistic encountered: [{statistic}]");
                }
            }
        }

        private static int GetSummonerSupportBonus(StatisticValueContext context, Statistics statistic)
        {
            if (!context.SummonerSupportRank.HasValue)
            {
                return 0;
            }

            switch (statistic)
            {
                case Statistics.HIT_POINTS:
                {
                    switch (context.SummonerSupportRank)
                    {
                        case SummonerSupportRanks.S:
                        {
                            return 5;
                        }

                        case SummonerSupportRanks.A:
                        case SummonerSupportRanks.B:
                        {
                            return 4;
                        }

                        case SummonerSupportRanks.C:
                        {
                            return 3;
                        }
                    }

                    break;
                }

                case Statistics.ATTACK:
                {
                    switch (context.SummonerSupportRank)
                    {
                        case SummonerSupportRanks.S:
                        {
                            return 2;
                        }

                        case SummonerSupportRanks.A:
                        case SummonerSupportRanks.B:
                        case SummonerSupportRanks.C:
                        {
                            return 0;
                        }
                    }

                    break;
                }

                case Statistics.SPEED:
                {
                    switch (context.SummonerSupportRank)
                    {
                        case SummonerSupportRanks.S:
                        case SummonerSupportRanks.A:
                        {
                            return 2;
                        }

                        case SummonerSupportRanks.B:
                        case SummonerSupportRanks.C:
                        {
                            return 0;
                        }
                    }

                    break;
                }

                case Statistics.DEFENSE:
                {
                    switch (context.SummonerSupportRank)
                    {
                        case SummonerSupportRanks.S:
                        case SummonerSupportRanks.A:
                        case SummonerSupportRanks.B:
                        {
                            return 2;
                        }

                        case SummonerSupportRanks.C:
                        {
                            return 0;
                        }
                    }

                    break;
                }

                case Statistics.RESISTANCE:
                {
                    switch (context.SummonerSupportRank)
                    {
                        case SummonerSupportRanks.S:
                        case SummonerSupportRanks.A:
                        case SummonerSupportRanks.B:
                        case SummonerSupportRanks.C:
                        {
                            return 2;
                        }
                    }

                    break;
                }

                default:
                {
                    throw new Exception($"Unexpected statistic encountered: [{statistic}]");
                }
            }

            return 0;
        }

        private static int GetMergeBonus(StatisticValueContext context, Statistics statistic)
        {
            if (context.Merges == 0)
            {
                return 0;
            }

            var baseStatisticValues = GetBaseValues(context);

            var statisticValueOrdinal = baseStatisticValues
                .OrderByDescending(x => x.AdjustedBaseValue)
                .ThenBy(x => x.Ordinal)
                .ToList()
                .FindIndex(x => x.Statistic == statistic);

            var mergeBonus = (int)Math.Floor((2 * context.Merges - statisticValueOrdinal - 1) / 5.0) + 1;

            if (!context.Flaw.HasValue && statisticValueOrdinal < 4)
            {
                mergeBonus++;
            }

            return mergeBonus;
        }

        private static int GetDragonflowerBonus(StatisticValueContext context, Statistics statistic)
        {
            if (context.Dragonflowers == 0)
            {
                return 0;
            }

            var baseStatisticValues = GetBaseValues(context);

            var statisticValueOrdinal = baseStatisticValues
                .OrderByDescending(x => x.AdjustedBaseValue)
                .ThenBy(x => x.Ordinal)
                .ToList()
                .FindIndex(x => x.Statistic == statistic);

            var dragonflowerBonus = (int)Math.Floor((context.Dragonflowers - statisticValueOrdinal - 1) / 5.0 + 1);

            return dragonflowerBonus;
        }

        private static int GetSkillBonus(StatisticValueContext context, Statistics statistic)
        {
            if (!context.IncludeSkillBonuses)
            {
                return 0;
            }

            var skillBonus = 0;

            if (context.Weapon != null)
            {
                skillBonus += GetSkillBonusHelper(context.Weapon, statistic);
            }

            if (context.PassiveA != null)
            {
                skillBonus += GetSkillBonusHelper(context.PassiveA, statistic);
            }

            if (context.SacredSeal != null)
            {
                skillBonus += GetSkillBonusHelper(context.SacredSeal, statistic);
            }

            return skillBonus;
        }

        private static int GetSkillBonusHelper(SkillValues skillValues, Statistics statistic)
        {
            switch (statistic)
            {
                case Statistics.HIT_POINTS:
                {
                    return skillValues.HitPointsModifier;
                }

                case Statistics.ATTACK:
                {
                    return skillValues.AttackModifier;
                }

                case Statistics.SPEED:
                {
                    return skillValues.SpeedModifier;
                }

                case Statistics.DEFENSE:
                {
                    return skillValues.DefenseModifier;
                }

                case Statistics.RESISTANCE:
                {
                    return skillValues.ResistanceModifier;
                }

                default:
                {
                    throw new Exception($"Unexpected statistic encountered: [{statistic}]");
                }
            }
        }
